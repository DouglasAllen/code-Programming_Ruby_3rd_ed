=begin

Array.drop

(from ~/.rdoc)
------------------------------------------------------------------------------
  ary.drop(n)               -> new_ary

------------------------------------------------------------------------------

Drops first n elements from ary and returns the rest of the elements in
an array.

  a = [1, 2, 3, 4, 5, 0]
  a.drop(3)             #=> [4, 5, 0]


