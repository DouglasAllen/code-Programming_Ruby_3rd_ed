Array.push

(from ~/.rdoc)
------------------------------------------------------------------------------
  ary.push(obj, ... )   -> ary

------------------------------------------------------------------------------

Append---Pushes the given object(s) on to the end of this array.
This expression returns the array itself, so several appends may be chained
together.

  a = [ "a", "b", "c" ]
  a.push("d", "e", "f")
          #=> ["a", "b", "c", "d", "e", "f"]

