=begin

Array.join

(from ~/.rdoc)
------------------------------------------------------------------------------
  ary.join(sep=$,)    -> str

------------------------------------------------------------------------------

Returns a string created by converting each element of the array to a string,
separated by sep.

  [ "a", "b", "c" ].join        #=> "abc"
  [ "a", "b", "c" ].join("-")   #=> "a-b-c"


